        private static HashMap<String, String> man_pages = new HashMap<String, String>();

        public static void show_man(String[] _splitted_input) {
                init_manual();
                if (_splitted_input.length == 1) {
                        System.out.println(ClientLoader.YELLOW_ANSI + cat_manual() + ClientLoader.RESET_ANSI);
                } else if (_splitted_input.length == 2 && man_pages.containsKey(_splitted_input[1])) {
                        System.out.println(ClientLoader.YELLOW_ANSI + man_pages.get(_splitted_input[1])
                                        + ClientLoader.RESET_ANSI);
                } else {
                        LoggedinSession.out_error_msg("unknown command.");
                }
        }

        private static void init_manual() {
                man_pages.put("lsum", lsum);
                man_pages.put("lsfr", lsfr);
                man_pages.put("lsau", lsau);
                man_pages.put("lslu", lslu);
                man_pages.put("lspf", lspf);
                man_pages.put("logout", logout);
                man_pages.put("mkdir", mkdir);
                man_pages.put("rmdir", rmdir);
                man_pages.put("cd", cd);
                man_pages.put("ls", ls);
                man_pages.put("rf", rf);
                man_pages.put("login", login);
                man_pages.put("reg", reg);
                man_pages.put("man", man);
                man_pages.put("up", up);
                man_pages.put("down", down);
        }

        private static String cat_manual() {
                String sep = "\n\n\n";
                return reg + sep + login + sep + logout + sep + up + sep + down + sep + mkdir + sep + rmdir + sep + cd
                                + sep + ls + sep + lsum + sep + lsfr + sep + lsau + sep + lslu + sep + lspf + sep + rf
                                + sep + man;
        }

